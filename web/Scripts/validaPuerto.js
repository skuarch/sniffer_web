

function validaPuerto(portNum){

    if(portNum==null || portNum==""){
        alert("Error:\n-El puerto debe de ser un numero entre 1 y 65532");
        return false;
    }


    if(portNum>65532){
        alert("Error:\n-El numero de puerto no puede ser mayor a 65532");
        return false;
    }


    if(isNaN(portNum)){
        alert("Error:\n-El puerto debe de ser un numero entre 1 y 65532");
        return false;
    }
    
    if(portNum == 0 || portNum == "0" || portNum < 1 ){
        alert("Error:\n-El puerto debe de ser un numero entre 1 y 65532");
        return false;
    }


    return true;

}